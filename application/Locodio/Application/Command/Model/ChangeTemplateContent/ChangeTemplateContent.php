declare(strict_types=1);

namespace App\Locodio\Application\Command\Model\ChangeTemplateContent;

class ChangeTemplateContent
{
    // ———————————————————————————————————————————————————————————————
    // Constructor
    // ———————————————————————————————————————————————————————————————

    private function __construct(
        protected int $templateId,
        protected int $masterTemplateId,
    ) {
    }

    // ———————————————————————————————————————————————————————————————
    // Hydration
    // ———————————————————————————————————————————————————————————————

    public static function hydrateFromJson($json): self
    {
        return new self(
            $json->templateId,
            $json->masterTemplateId,
        );
    }

    // ———————————————————————————————————————————————————————————————
    // Getters
    // ———————————————————————————————————————————————————————————————

    public function getTemplateId(): int
    {
        return $this->templateId;
    }

    public function getMasterTemplateId(): int
    {
        return $this->masterTemplateId;
    }
}
