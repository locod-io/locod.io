declare(strict_types=1);

namespace App\Locodio\Application\Command\Model\ImportTemplatesFromMasterTemplates;

class ImportTemplatesFromMasterTemplates
{
    // ———————————————————————————————————————————————————————————————
    // Constructor
    // ———————————————————————————————————————————————————————————————

    private function __construct(
        protected int   $projectId,
        protected array $masterTemplateIds
    ) {
    }

    // ———————————————————————————————————————————————————————————————
    // Hydration
    // ———————————————————————————————————————————————————————————————

    public static function hydrateFromJson(\stdClass $json): self
    {
        return new self(
            $json->projectId,
            $json->masterTemplateIds
        );
    }

    // ———————————————————————————————————————————————————————————————
    // Getters
    // ———————————————————————————————————————————————————————————————

    public function getProjectId(): int
    {
        return $this->projectId;
    }

    public function getMasterTemplateIds(): array
    {
        return $this->masterTemplateIds;
    }
}
